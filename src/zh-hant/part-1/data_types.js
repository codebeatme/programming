/// 常見的資料型別
// 數字型別
let num1 = 100
// 字串型別
let str1 = '你好啊！'
// 布林型別
let bool1 = false

/// 數字型別和字串型別的加法操作
// num2 的值將為 2
let num2 = 1 + 1
// str2 的值將為 '11'
let str2 = '1' + '1'